const categorySlug = new Map(categoryOrder.map((name, index) => [name, `${String(index + 1).padStart(2, "0")}`]));
const phrases = [];
const usedIds = new Set();

function idPart(text) {
  return text
    .normalize("NFKD")
    .replace(/[^\p{Letter}\p{Number}]+/gu, "_")
    .replace(/^_+|_+$/g, "")
    .slice(0, 48)
    .toLowerCase();
}

function uniq(values) {
  return [...new Set(values.filter(Boolean).map((value) => String(value).trim()).filter(Boolean))];
}

function koTokens(text) {
  return text
    .replace(/[?.!,。！？]/g, " ")
    .split(/\s+/)
    .flatMap((token) => {
      const clean = token.trim();
      const stripped = clean.replace(/(은|는|이|가|을|를|에|에서|으로|로|도|만|요|습니다|해요|세요)$/g, "");
      return clean === stripped ? [clean] : [clean, stripped];
    })
    .filter((token) => token.length > 1);
}

function hasKoreanFinalConsonant(text) {
  const chars = [...text.trim()];
  for (let index = chars.length - 1; index >= 0; index -= 1) {
    const code = chars[index].charCodeAt(0);
    if (code >= 0xac00 && code <= 0xd7a3) {
      return (code - 0xac00) % 28 !== 0;
    }
    if (/[A-Za-z0-9]/.test(chars[index])) return false;
  }
  return false;
}

function topic(text) {
  return `${text}${hasKoreanFinalConsonant(text) ? "은" : "는"}`;
}

function subject(text) {
  return `${text}${hasKoreanFinalConsonant(text) ? "이" : "가"}`;
}

function object(text) {
  return `${text}${hasKoreanFinalConsonant(text) ? "을" : "를"}`;
}

function add(category, ko, ja, pronunciation, note = "", tags = [], synonyms = [], priority = 3) {
  if (!categorySlug.has(category)) {
    throw new Error(`Unknown category: ${category}`);
  }

  const baseId = `${categorySlug.get(category)}_${idPart(ko) || phrases.length}`;
  let id = baseId;
  let suffix = 2;
  while (usedIds.has(id)) {
    id = `${baseId}_${suffix++}`;
  }
  usedIds.add(id);

  phrases.push({
    id,
    category,
    ko,
    ja,
    pronunciation,
    note,
    tags: uniq([category, ...koTokens(ko), ...tags]),
    synonyms: uniq(synonyms),
    priority,
  });
}

function addMany(rows) {
  for (const row of rows) add(...row);
}

