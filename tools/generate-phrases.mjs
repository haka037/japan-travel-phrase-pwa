function addTermTemplates(category, terms, templates) {
  for (const term of terms) {
    for (const template of templates) {
      add(
        category,
        template.ko(term),
        template.ja(term),
        template.pronunciation(term),
        template.note,
        [term.ko, ...(template.tags || [])],
        template.synonyms ? template.synonyms(term) : [],
        template.priority ?? 3
      );
    }
  }
}

